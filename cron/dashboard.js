    'var confirmDeleteId = null;',
    'var DAY_NAMES = ["周日","周一","周二","周三","周四","周五","周六"];',
    'var DAY_KEYS = [0,1,2,3,4,5,6];',
    '',
    '// --- utils ---',
    'function esc(s) { return String(s||"").replace(/&/g,"&amp;").replace(/</g,"&lt;").replace(/>/g,"&gt;"); }',
    'function escAttr(s) { return String(s||"").replace(/&/g,"&amp;").replace(/"/g,"&quot;").replace(/</g,"&lt;").replace(/>/g,"&gt;"); }',
    'function fmt(s) { if (!s) return "-"; try { var d=new Date(s+(s.indexOf("Z")>=0?"":"Z")); return d.toLocaleString("zh-CN",{month:"2-digit",day:"2-digit",hour:"2-digit",minute:"2-digit"}); } catch(e){return s;} }',
    '',
    '// --- cron human preview ---',
    'var MONTH_NAMES = ["","1月","2月","3月","4月","5月","6月","7月","8月","9月","10月","11月","12月"];',
    'var DOW_NAMES = ["周日","周一","周二","周三","周四","周五","周六"];',
    '',
    'function fieldToList(val) {',
    '  if (!val || val === "*") return [];',
    '  var result = [];',
    '  var parts = val.split(",");',
    '  for (var i = 0; i < parts.length; i++) {',
    '    var p = parts[i];',
    '    if (p.indexOf("-") >= 0) {',
    '      var r = p.split("-");',
    '      for (var j = parseInt(r[0],10); j <= parseInt(r[1],10); j++) result.push(j);',
    '    } else {',
    '      result.push(parseInt(p,10));',
    '    }',
    '  }',
    '  return result;',
    '}',
    '',
    'function listToHuman(list, labels) {',
    '  if (!list.length) return "";',
    '  return list.map(function(v){ return labels ? labels[v] : v; }).join("、");',
    '}',
    '',
    'function cronToHuman(expr) {',
    '  if (!expr) return "";',
    '  var parts = expr.trim().split(/\\s+/);',
    '  if (parts.length !== 5) return expr;',
    '  var min = parts[0], hour = parts[1], dom = parts[2], month = parts[3], dow = parts[4];',
    '  var h = parseInt(hour,10), m = parseInt(min,10);',
    '  var ts = String(h).padStart(2,"0") + ":" + String(m).padStart(2,"0");',
    '',
    '  // Daily: * * * * *',
    '  if (dom === "*" && month === "*") {',
    '    if (dow === "*") return "每天 " + ts;',
    '    if (dow === "1-5") return "工作日 " + ts;',
    '    if (/^[0-7]$/.test(dow)) return "每" + DOW_NAMES[parseInt(dow)%7] + " " + ts;',
    '    var dowList = fieldToList(dow);',
    '    if (dowList.length) return "每周" + listToHuman(dowList, DOW_NAMES) + " " + ts;',
    '  }',
    '',
