    'function compressRanges(vals, min, max) {',
    '  if (!vals || !vals.length) return "*";',
    '  var sorted = vals.slice().sort(function(a,b){return a-b;});',
    '  if (sorted.length === (max - min + 1)) return "*";',
    '  var parts = []; var i = 0;',
    '  while (i < sorted.length) {',
    '    var start = sorted[i]; var end = start;',
    '    while (i + 1 < sorted.length && sorted[i + 1] === end + 1) { end = sorted[++i]; }',
    '    if (end - start >= 2) parts.push(start + "-" + end);',
    '    else if (end > start) parts.push(start + "," + end);',
    '    else parts.push(String(start));',
    '    i++;',
    '  }',
    '  return parts.join(",");',
    '}',
